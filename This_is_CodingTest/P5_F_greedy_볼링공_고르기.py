""" 그리디 : 5.볼링공 고르기 """

def bowlingBall():
    from itertools import combinations
    n, m = map(int, input().split())
    weight = list(map(int, input().split()))
    count = 0

    for comb in combinations(range(n), 2):
        if weight[comb[0]] != weight[comb[1]]:
            count += 1
            # print(comb)

    print(count)

bowlingBall()


def bowlingBall_ans():
    n, m = map(int, input().split())
    balls = list(map(int, input().split()))
    weight = [0] * (m+1)

    for b in balls:
        weight[b] += 1
    result = 0

    # 조합이라서 아래 방식의 코드 사용
    for i in range(1, m+1):
        n -= weight[i]
        result += weight[i] * n
    # 이해 안되면 다시 구글링 해보기

    print(result)

bowlingBall_ans()

